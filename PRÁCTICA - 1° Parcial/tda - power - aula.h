//TDA MATEMÁTICO -suma/Resta/Multiplicación

//INTERFACE
//SUMA
float SUMA(float a, float b);
//RESTA
float RESTA(float a, float b);
//MULTIPLICACIÓN
float MULTI(float a, float b);


//SUMA
float SUMA(float a, float b){
    float suma = a + b;
    return suma;
}

//RESTA
float RESTA(float a, float b){
    float resta = a - b;
    return resta;
}

//MULTIPLICACIÓN
float MULTI(float a, float b){
    float multi = a * b;
    return multi;
}